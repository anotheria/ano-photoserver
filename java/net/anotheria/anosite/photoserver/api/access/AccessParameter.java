package net.anotheria.anosite.photoserver.api.access;

/**
 * Access parameter.
 *
 * @author ivanbatura
 */
public enum AccessParameter {
    /**
     * Source.
     */
    SOURCE,

    /**
     * Access token.
     */
    ACCESS_TOKEN,

    /**
     * Blur flag.
     */
    BLUR,

    /**
     * Preview flag.
     */
    PREVIEW,

    /**
     * Full access cookie name.
     */
    ACCESS_COOKIE_NAME
}
